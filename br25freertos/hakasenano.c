void __attribute__((interrupt)) UART2_IntHandler(void) {
	if (JL_UART2->CON0 & (1<<14)) {
		JL_UART2->CON0 |= (1<<12);	/* clr rx int */

		char ch = JL_UART2->BUF;
		if (ch == '\r' || ch == '\n') {
			nchars = 0;
		} else if (ch == '\b' || ch == '\x7f') {
			if (nchars > 0)
				nchars--;
		} else {
			marqcounter = 0;
			if (nchars < sizeof(chars))
				chars[nchars++] = ch;
		}
	}
}




SemaphoreHandle_t timer4sem;


void __attribute__((interrupt)) Timer4_IntHandler(void) {
	JL_TIMER4->CON |= (1<<14);	/* clr int */

	BaseType_t hpwoke = pdFALSE;
	xSemaphoreGiveFromISR(timer4sem, &hpwoke);
	portEND_SWITCHING_ISR(hpwoke);
}



void drawtext(int x, int y, char *str, uint16_t color) {
	dbi_select(1);

	dbi_send_cmd(0x3A);
	dbi_send_data(0x05);

	dbi_send_cmd(0x36);
	dbi_send_data(
		(0 << 7)|	/* MY    - reverse Y axis direction (memory write) */
		(1 << 6)|	/* MX    - reverse X axis direction (memory write) */
		(1 << 5)|	/* MV    - swap X/Y axis (memory write) */
		(0 << 3)	/* BGR   - swap R/B channels */
	);

	int cx = x, cy = y;
	uint16_t blk[8][6];

	while (*str) {
		char c = *str++;

		switch (c) {
		case '\n':
			cy += 8;
		case '\r':
			cx = x;
			break;

		case '\b':
			cx -= 6;
			break;

		default:
			memset(blk, 0, sizeof blk);

			for (int x = 0; x < 5; x++) {
				for (int y = 0; y < 7; y++) {
					if (the_font[y + (c - ' ') * 7] & (0x80 >> x))
						blk[y][x] = color;
				}
			}

			dbi_setwindow(cx, cy, 6, 8);
			dbi_send_cmd(0x2C);
			dbi_send_data_buff(blk, sizeof blk);

			cx += 6;
			break;
		}
	}

	dbi_select(0);
}




